function generateAppEntry(model: SemanticAppModel): GeneratedFile {
    const appName = pascalCase(model.appName ?? 'MyApp');
    const lines: string[] = [];

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import SwiftUI');
    lines.push('');
    lines.push('@main');
    lines.push(`struct ${appName}App: App {`);
    lines.push('    var body: some Scene {');
    lines.push('        WindowGroup {');
    lines.push('            ContentView()');
    lines.push('        }');
    lines.push('    }');
    lines.push('}');
    lines.push('');

    return {
        path: `${appName}App.swift`,
        content: lines.join('\n'),
        sourceMapping: 'morphkit:project',
        confidence: 'high',
        warnings: [],
    };
}


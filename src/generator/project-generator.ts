function generateAppEntry(model: SemanticAppModel): GeneratedFile {
    const appName = pascalCase(model.appName ?? 'MyApp');
    const lines: string[] = [];

    const eligible = getSwiftDataEligibleEntities(model);
    const hasSwiftData = eligible.length > 0;

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import SwiftUI');
    if (hasSwiftData) lines.push('import SwiftData');
    lines.push('');
    lines.push('@main');
    lines.push(`struct ${appName}App: App {`);
    lines.push('    var body: some Scene {');
    lines.push('        WindowGroup {');
    lines.push('            ContentView()');
    lines.push('        }');
    if (hasSwiftData) {
        const storeTypes = eligible.map(e => `${pascalCase(e.name)}Record.self`);
        lines.push(`        .modelContainer(for: [${storeTypes.join(', ')}])`);
    }
    lines.push('    }');
    lines.push('}');
    lines.push('');

    return {
        path: `${appName}App.swift`,
        content: lines.join('\n'),
        sourceMapping: 'morphkit:project',
        confidence: 'high',
        warnings: [],
    };
}


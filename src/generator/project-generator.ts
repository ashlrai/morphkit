/**
 * Generate the default fetch method lines for a store.
 * Used by both Zustand-derived stores (when no mutations defined) and
 * entity-based stores to avoid duplicating the fetch boilerplate.
 */
function generateDefaultFetchMethod(storeName: string, model?: SemanticAppModel): string[] {
    const varName = storeName.charAt(0).toLowerCase() + storeName.slice(1);
    const lines: string[] = [];

    // Check if a plural fetch method (fetchProducts()) would exist in the generated APIClient
    // The APIClient generates fetchX() from endpoints where X matches the resource path segment
    const endpoints = model?.apiEndpoints ?? [];
    const pluralName = pluralize(storeName);
    // A plural fetch method exists when there's a GET endpoint whose path ends with the
    // plural resource name (e.g., /products, /api/products) AND has no path params
    // (parameterized endpoints like /posts/:id/comments generate fetchComment(postId:), not fetchComments())
    const hasFetchMethod = endpoints.some(ep => {
        if ((ep.method ?? 'GET') !== 'GET') return false;
        // Clean the URL: strip template literals, backticks, ${VAR} prefixes
        const url = (ep.url ?? '').replace(/`/g, '').replace(/\$\{[^}]+\}/g, '').toLowerCase();
        // Skip endpoints with path parameters
        if (url.includes(':') || url.includes('{') || url.includes('[')) return false;
        const lastSegment = url.split('/').filter(s => s && s !== 'api').pop() ?? '';
        return lastSegment === varName + 's' || lastSegment === varName;
    });

    lines.push('');
    lines.push(`    func fetch${pluralName}() async {`);
    lines.push('        isLoading = true');
    lines.push('        error = nil');
    lines.push('');

    if (hasFetchMethod) {
        lines.push('        do {');
        lines.push(`            ${varName}s = try await apiClient.fetch${pluralName}()`);
        lines.push('        } catch let networkError as NetworkError {');
        lines.push('            error = networkError');
        lines.push('        } catch {');
        lines.push('            self.error = .networkError(URLError(.unknown))');
        lines.push('        }');
    } else {
        // No matching API endpoint — generate a placeholder that compiles
        lines.push(`        // Fetch ${varName}s from your API`);
        lines.push(`        // apiClient.fetch${pluralName}() is not yet configured`);
        lines.push(`        // Add the endpoint to your API and uncomment:`);
        lines.push(`        // ${varName}s = try await apiClient.fetch${pluralName}()`);
    }

    lines.push('');
    lines.push('        isLoading = false');
    lines.push('    }');
    return lines;
}

/**
 * Generate the standard store boilerplate: collection property, selected item,
 * loading/error state, APIClient dependency, and initializer.
 */
function generateStoreBoilerplate(storeName: string): string[] {
    const varName = storeName.charAt(0).toLowerCase() + storeName.slice(1);
    const lines: string[] = [];
    lines.push(`    var ${varName}s: [${storeName}] = []`);
    lines.push(`    var selected${storeName}: ${storeName}?`);
    lines.push('    var isLoading = false');
    lines.push('    var error: NetworkError?');
    lines.push('');
    lines.push('    private let apiClient: APIClient');
    lines.push('');
    lines.push('    init(apiClient: APIClient = .shared) {');
    lines.push('        self.apiClient = apiClient');
    lines.push('    }');
    return lines;
}

/**
 * Determine which entity names have associated API endpoints.
 * An entity is considered API-backed if any endpoint URL contains the
 * entity name (lowercase, singular or plural) or the response type
 * references it.
 */
function entitiesWithApiEndpoints(model: SemanticAppModel): Set<string> {
    const result = new Set<string>();
    const endpoints = model.apiEndpoints ?? [];

    for (const entity of model.entities ?? []) {
        const nameLower = entity.name.toLowerCase();
        for (const ep of endpoints) {
            const urlLower = ep.url.toLowerCase();
            // Check URL contains entity name (singular or plural)
            if (urlLower.includes(nameLower) || urlLower.includes(nameLower + 's')) {
                result.add(entity.name);
                break;
            }
            // Check response type references entity
            if (ep.responseType?.typeName?.toLowerCase() === nameLower) {
                result.add(entity.name);
                break;
            }
        }
    }

    return result;
}

/**
 * Detect sub-type entities that are used as fields of other entities
 * (e.g. CartItem is a sub-type of Cart because Cart has a CartItem[] field).
 * These should not get their own store.
 */
function getSubTypeEntityNames(model: SemanticAppModel): Set<string> {
    const subTypes = new Set<string>();
    const entities = model.entities ?? [];
    const entityNames = new Set(entities.map((e) => e.name));

    for (const entity of entities) {
        for (const field of entity.fields ?? []) {
            // Check if a field references another entity by name (direct or array)
            const referencedName =
                field.type.typeName ??
                field.type.elementType?.typeName;
            if (referencedName && entityNames.has(referencedName) && referencedName !== entity.name) {
                subTypes.add(referencedName);
            }
        }
    }

    return subTypes;
}

/**
 * Resolve a Swift type for a Zustand store field by matching against model entities.
 * Falls back to heuristic type inference based on common field names.
 */
function resolveFieldType(
    fieldName: string,
    storeName: string,
    model: SemanticAppModel,
): string {
    const entities = model.entities ?? [];
    const entityNames = new Set(entities.map((e) => e.name));
    const lower = fieldName.toLowerCase();

    // "items" → look for a matching sub-entity (e.g. CartItem for Cart store)
    if (lower === 'items') {
        const itemEntity = `${storeName}Item`;
        if (entityNames.has(itemEntity)) return `[${itemEntity}] = []`;
        // Fallback: look for any entity with "Item" in name
        for (const e of entities) {
            if (e.name.endsWith('Item')) return `[${e.name}] = []`;
        }
        return '[Any] = [] // Replace with concrete item type';
    }

    // Numeric field names
    if (lower === 'total' || lower === 'subtotal' || lower === 'price' || lower === 'amount') {
        return 'Double = 0';
    }
    if (lower === 'quantity' || lower === 'count') {
        return 'Int = 0';
    }

    // Boolean field names
    if (lower.startsWith('is') || lower.startsWith('has') || lower === 'loading' || lower === 'added') {
        return 'Bool = false';
    }

    // String field names
    if (lower === 'error' || lower === 'message') {
        return 'String?';
    }

    // Try to match field name to an entity (e.g. "product" → Product?)
    const pascalField = fieldName.charAt(0).toUpperCase() + fieldName.slice(1);
    if (entityNames.has(pascalField)) {
        return `${pascalField}?`;
    }

    // Plural field → array of entity (e.g. "products" → [Product])
    if (lower.endsWith('s') && lower.length > 1) {
        const singular = pascalField.slice(0, -1);
        if (entityNames.has(singular)) return `[${singular}] = []`;
    }

    return 'Any // Replace with concrete type';
}

/**
 * Resolve an array element type from a screen name by finding a matching entity.
 * e.g. "Products" screen → "Product" entity, "Cart" screen → "CartItem" entity.
 */
function resolveArrayTypeFromScreenName(
    screenName: string,
    model: SemanticAppModel,
): string {
    const entities = model.entities ?? [];
    const entityNames = new Set(entities.map((e) => e.name));
    const pascal = pascalCase(screenName);

    // Try singular form: "Products" → "Product"
    if (pascal.endsWith('s') && pascal.length > 1) {
        const singular = pascal.slice(0, -1);
        if (entityNames.has(singular)) return singular;
    }

    // Try exact match
    if (entityNames.has(pascal)) return pascal;

    // Try with "Item" suffix: "Cart" → "CartItem"
    const itemName = `${pascal}Item`;
    if (entityNames.has(itemName)) return itemName;

    return 'Any';
}

function generateStateLayer(model: SemanticAppModel): GeneratedFile[] {
    const files: GeneratedFile[] = [];
    const screens = (model.screens ?? []).filter((s) => !isMarketingScreen(s));
    const statePatterns = model.stateManagement ?? [];

    // Determine which entities have API endpoints and which are sub-types
    const apiBackedEntities = entitiesWithApiEndpoints(model);
    const subTypeEntities = getSubTypeEntityNames(model);

    // --- Strategy 1: Zustand stores → consolidate into one store per web store ---
    // Each Zustand state pattern maps to a single Swift @Observable store.
    const zustandPatterns = statePatterns.filter((sp) => sp.source === 'zustand');
    const generatedStoreNames = new Set<string>();

    for (const sp of zustandPatterns) {
        const storeName = cleanStoreName(sp.name);
        // Skip if we already generated a store with this cleaned name
        if (generatedStoreNames.has(storeName)) continue;
        generatedStoreNames.add(storeName);

        const lines: string[] = [];
        lines.push(`// Generated by Morphkit from Zustand store: ${sp.name}`);
        lines.push('');
        lines.push('import SwiftUI');
        lines.push('import Observation');
        lines.push('');
        lines.push('@Observable');
        lines.push(`final class ${storeName}Store {`);

        // Add state properties from the Zustand shape (deduplicated)
        if (sp.shape?.fields) {
            const seenFieldNames = new Set<string>();
            for (const field of sp.shape.fields) {
                const propName = field.name.charAt(0).toLowerCase() + field.name.slice(1);
                if (seenFieldNames.has(propName)) continue;
                seenFieldNames.add(propName);

                // Try to resolve a concrete Swift type from model entities
                const resolvedType = resolveFieldType(propName, storeName, model);
                lines.push(`    var ${propName}: ${resolvedType}`);
            }
        } else {
            // Fallback: use store name to infer a collection property
            const varName = storeName.charAt(0).toLowerCase() + storeName.slice(1);
            lines.push(`    var ${varName}s: [${storeName}] = []`);
            lines.push(`    var selected${storeName}: ${storeName}?`);
        }

        // Only add isLoading/error if not already declared from shape fields
        const shapeFieldNames = sp.shape?.fields
            ? new Set(sp.shape.fields.map((f) => f.name.charAt(0).toLowerCase() + f.name.slice(1)))
            : new Set<string>();
        if (!shapeFieldNames.has('isLoading')) {
            lines.push('    var isLoading = false');
        }
        if (!shapeFieldNames.has('error')) {
            lines.push('    var error: NetworkError?');
        }
        lines.push('');
        lines.push('    private let apiClient: APIClient');
        lines.push('');
        lines.push('    init(apiClient: APIClient = .shared) {');
        lines.push('        self.apiClient = apiClient');
        lines.push('    }');

        // Add mutation methods from Zustand actions
        if (sp.mutations && sp.mutations.length > 0) {
            lines.push('');
            for (const mutation of sp.mutations) {
                const methodName = mutation.name.charAt(0).toLowerCase() + mutation.name.slice(1);
                lines.push(`    func ${methodName}() async {`);
                lines.push('        print("\\(#function) called")');
                lines.push('    }');
            }
        } else {
            lines.push(...generateDefaultFetchMethod(storeName, model));
        }

        lines.push('}');
        lines.push('');

        files.push({
            path: `State/${storeName}Store.swift`,
            content: lines.join('\n'),
            sourceMapping: sp.consumers?.[0] ?? 'unknown',
            confidence: 'medium',
            warnings: [
                `${storeName}Store generated from Zustand store "${sp.name}" — verify API methods`,
            ],
        });
    }

    // --- Strategy 2: Entity-based stores for non-Zustand apps ---
    // Only generate stores for entities that have API endpoints and are not sub-types.
    // Skip if Zustand stores already cover these entities.
    if (zustandPatterns.length === 0) {
        const entities = model.entities ?? [];

        for (const entity of entities) {
            // Skip enum entities (single __enum field)
            if (entity.fields.length === 1 && entity.fields[0]?.name === '__enum') continue;
            // Skip entities without API endpoints
            if (!apiBackedEntities.has(entity.name)) continue;
            // Skip sub-type entities (e.g. CartItem is part of Cart)
            if (subTypeEntities.has(entity.name)) continue;
            // Skip if a store with this name was already generated
            const name = pascalCase(entity.name);
            if (generatedStoreNames.has(name)) continue;
            generatedStoreNames.add(name);

            const lines: string[] = [];

            lines.push(`// Generated by Morphkit from: ${relativeSourcePath(entity.sourceFile ?? 'unknown')}`);
            lines.push('');
            lines.push('import SwiftUI');
            lines.push('import Observation');
            lines.push('');
            lines.push('@Observable');
            lines.push(`final class ${name}Store {`);
            lines.push(...generateStoreBoilerplate(name));
            lines.push(...generateDefaultFetchMethod(name, model));
            lines.push('}');
            lines.push('');

            files.push({
                path: `State/${name}Store.swift`,
                content: lines.join('\n'),
                sourceMapping: entity.sourceFile ?? 'unknown',
                confidence: 'medium',
                warnings: [
                    `${name}Store uses inferred API methods — verify method names match APIClient`,
                ],
            });
        }
    }

    // Generate per-screen ViewModels that group local state bindings
    for (const screen of screens) {
        if (screen.stateBindings.length === 0) continue;

        const viewModelName = `${pascalCase(screen.name)}ViewModel`;
        const lines: string[] = [];

        lines.push(`// Generated by Morphkit — ViewModel for ${screen.name} screen`);
        lines.push('');
        lines.push('import SwiftUI');
        lines.push('import Observation');
        lines.push('');
        lines.push('@Observable');
        lines.push(`final class ${viewModelName} {`);

        const declaredProps = new Set<string>();
        for (const binding of screen.stateBindings) {
            const propName = binding.charAt(0).toLowerCase() + binding.slice(1);
            // Skip duplicate property names
            if (declaredProps.has(propName)) continue;
            declaredProps.add(propName);

            // Infer type from the binding name
            const lower = binding.toLowerCase();
            if (lower.startsWith('is') || lower.startsWith('has') || lower.startsWith('show') || lower.startsWith('should') || lower === 'added' || lower.includes('loading') || lower.includes('loaded') || lower.includes('saving')) {
                lines.push(`    var ${propName} = false`);
            } else if (lower.includes('query') || lower.includes('search') || lower.includes('text') || lower.includes('input') || lower.includes('name') || lower.includes('email') || lower.includes('password')) {
                lines.push(`    var ${propName} = ""`);
            } else if (lower.includes('order') || lower.includes('sort') || lower.includes('category') || lower.includes('filter') || lower.includes('mode') || lower.includes('status') || lower.includes('type') || lower.includes('view')) {
                lines.push(`    var ${propName} = ""`);
            } else if (lower.includes('count') || lower.includes('index') || lower.includes('page') || lower.includes('total')) {
                lines.push(`    var ${propName} = 0`);
            } else if (lower.includes('error') || lower.includes('message')) {
                lines.push(`    var ${propName}: String?`);
            } else if (lower.includes('items') || lower.includes('list') || lower.includes('results') || lower.includes('ids') || lower.includes('collections')) {
                const entityType = resolveArrayTypeFromScreenName(screen.name, model);
                lines.push(`    var ${propName}: [${entityType}] = []`);
            } else if (lower.includes('id') || lower.includes('url') || lower.includes('token')) {
                lines.push(`    var ${propName}: String?`);
            } else if (lower.includes('date') || lower.includes('range')) {
                lines.push(`    var ${propName}: Date?`);
            } else {
                // Default to String for safety
                lines.push(`    var ${propName} = ""`);
            }
        }

        // Only add isLoading if not already declared from stateBindings
        if (!declaredProps.has('isLoading')) {
            lines.push('    var isLoading = false');
        }
        if (!declaredProps.has('error')) {
            lines.push('    var error: NetworkError?');
        }
        lines.push('}');
        lines.push('');

        files.push({
            path: `State/${viewModelName}.swift`,
            content: lines.join('\n'),
            sourceMapping: screen.sourceFile ?? 'unknown',
            confidence: 'medium',
            warnings: [
                `${viewModelName} state types are inferred — verify and replace with concrete types`,
            ],
        });
    }

    // --- AuthManager: generated when auth pattern is detected ---
    if (model.auth) {
        files.push({
            path: 'State/AuthManager.swift',
            content: generateAuthManager(model),
            sourceMapping: 'morphkit:auth',
            confidence: 'high',
            warnings: [],
        });
    }

    return files;
}


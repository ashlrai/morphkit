// ---------------------------------------------------------------------------
// Orchestrator
// ---------------------------------------------------------------------------

export async function generateXcodeProject(
    model: SemanticAppModel,
    outputPath: string,
): Promise<GeneratedProject> {
    const appName = pascalCase(model.appName ?? 'MyApp');
    const appDir = join(outputPath, appName);
    const allFiles: GeneratedFile[] = [];
    const allWarnings: string[] = [];

    // --- Run all generators ---

    // 1. Models
    const modelFiles = generateSwiftModels(model);
    allFiles.push(...modelFiles);

    // 2. Views
    const viewFiles = generateSwiftUIViews(model);
    allFiles.push(...viewFiles);

    // 3. Navigation
    const navFiles = generateNavigation(model);
    allFiles.push(...navFiles);

    // 4. Networking
    const networkFiles = generateNetworkingLayer(model);
    allFiles.push(...networkFiles);

    // 5. State
    const stateFiles = generateStateLayer(model);
    allFiles.push(...stateFiles);

    // 6. App entry
    allFiles.push(generateAppEntry(model));

    // 7. Info.plist
    allFiles.push(generateInfoPlist(model));

    // 8. Assets
    const assetFiles = generateAssets(model);
    allFiles.push(...assetFiles);

    // 9. Tests
    const testFiles = generateTestStubs(model);
    allFiles.push(...testFiles);

    // 10. Xcode project note
    allFiles.push(generateXcodeProjNote(model));

    // --- Calculate stats ---

    const stats: GeneratedProject['stats'] = {
        totalFiles: allFiles.length,
        highConfidence: allFiles.filter((f) => f.confidence === 'high').length,
        mediumConfidence: allFiles.filter((f) => f.confidence === 'medium').length,
        lowConfidence: allFiles.filter((f) => f.confidence === 'low').length,
        warnings: [],
    };

    // Collect all warnings
    for (const file of allFiles) {
        for (const w of file.warnings) {
            stats.warnings.push(`[${file.path}] ${w}`);
        }
    }

    // 11. README (needs stats)
    allFiles.push(generateReadme(model, stats));

    // Update total
    stats.totalFiles = allFiles.length;

    // --- Write files to disk ---

    for (const file of allFiles) {
        const fullPath = join(appDir, file.path);
        const dir = dirname(fullPath);

        await mkdir(dir, { recursive: true });
        await writeFile(fullPath, file.content, 'utf-8');
    }

    return {
        appName,
        files: allFiles,
        outputPath: appDir,
        stats,
    };
}

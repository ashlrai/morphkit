/**
 * Build a ModelDiff for the first sync (no previous model).
 * Everything is "added".
 */
function buildFirstSyncDiff(model: SemanticAppModel): ModelDiff {
  return {
    addedScreens: model.screens.map(s => s.name),
    removedScreens: [],
    modifiedScreens: [],
    addedEntities: model.entities.map(e => e.name),
    removedEntities: [],
    modifiedEntities: [],
    addedEndpoints: model.apiEndpoints.map(ep => `${ep.method}:${ep.url}`),
    removedEndpoints: [],
    modifiedEndpoints: [],
    changedNavigation: true,
    changedAuth: model.auth !== null,
    summary: `Initial sync: ${model.screens.length} screens, ${model.entities.length} entities, ${model.apiEndpoints.length} endpoints`,
  };
}

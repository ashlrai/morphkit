function generateTabEnum(model: SemanticAppModel): string {
    // Always use inferTabs to get all top-level routes as tabs
    // model.navigation.tabs may be incomplete (builder only sets entry point tabs)
    const tabs = inferTabs(model);
    const lines: string[] = [];

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import SwiftUI');
    lines.push('');
    lines.push('enum AppTab: String, CaseIterable {');

    for (const tab of tabs) {
        lines.push(`    case ${camelCase(tab.label)}`);
    }

    lines.push('');
    lines.push('    var title: String {');
    lines.push('        switch self {');
    for (const tab of tabs) {
        lines.push(`        case .${camelCase(tab.label)}: "${pascalCase(tab.label)}"`);
    }
    lines.push('        }');
    lines.push('    }');
    lines.push('');
    lines.push('    var systemImage: String {');
    lines.push('        switch self {');
    for (const tab of tabs) {
        lines.push(`        case .${camelCase(tab.label)}: "${tab.icon ?? inferIcon(tab.label)}"`);
    }
    lines.push('        }');
    lines.push('    }');
    lines.push('}');

    return lines.join('\n');
}

function inferTabs(model: SemanticAppModel): TabItem[] {
    const routes = model.navigation?.routes ?? [];
    const screens = model.screens ?? [];

    // Top-level routes: those with 0 or 1 path segments and no dynamic params
    const topLevelRoutes = routes.filter((r) => {
        const segments = r.path.split('/').filter(Boolean);
        return segments.length <= 1 && r.params.length === 0;
    });

    // Deduplicate by screen name and limit to 5
    const seenScreens = new Set<string>();
    const tabs: TabItem[] = [];
    for (const route of topLevelRoutes) {
        if (seenScreens.has(route.screen)) continue;
        seenScreens.add(route.screen);
        tabs.push({
            label: route.screen,
            icon: inferIcon(route.screen),
            screen: route.screen,
        });
        if (tabs.length >= 5) break;
    }

    if (tabs.length === 0) {
        // Fallback: use screens that look top-level (entry point or first few)
        const entryScreens = screens.filter((s) => s.isEntryPoint);
        if (entryScreens.length > 0) {
            return entryScreens.slice(0, 5).map((s) => ({
                label: s.name,
                icon: inferIcon(s.name),
                screen: s.name,
            }));
        }
        return [{ label: 'Home', icon: 'house', screen: screens[0]?.name ?? 'Home' }];
    }

    return tabs;
}

function inferIcon(name: string): string {
    const lower = name.toLowerCase();
    const iconMap: Record<string, string> = {
        home: 'house',
        dashboard: 'square.grid.2x2',
        settings: 'gearshape',
        profile: 'person',
        account: 'person.circle',
        search: 'magnifyingglass',
        favorites: 'heart',
        cart: 'cart',
        orders: 'list.clipboard',
        messages: 'message',
        chat: 'bubble.left.and.bubble.right',
        notifications: 'bell',
        discover: 'safari',
        explore: 'binoculars',
        activity: 'chart.bar',
        analytics: 'chart.line.uptrend.xyaxis',
        feed: 'newspaper',
        map: 'map',
        calendar: 'calendar',
        contacts: 'person.2',
        products: 'bag',
        categories: 'square.grid.3x3',
        more: 'ellipsis.circle',
    };

    for (const [key, icon] of Object.entries(iconMap)) {
        if (lower.includes(key)) return icon;
    }

    return 'circle';
}


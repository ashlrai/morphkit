export async function generateXcodeProject(
    model: SemanticAppModel,
    outputPath: string,
): Promise<GeneratedProject> {
    const appName = pascalCase(model.appName ?? 'MyApp');
    const appDir = join(outputPath, appName);
    const allFiles: GeneratedFile[] = [];
    const allWarnings: string[] = [];

    // --- Run all generators ---

    // 1. Models (Codable structs)
    const modelFiles = generateSwiftModels(model);
    allFiles.push(...modelFiles);

    // 1b. SwiftData persistence models
    const swiftDataFiles = generateSwiftDataModels(model);
    allFiles.push(...swiftDataFiles);

    // 1c. DataManager (save/fetch for SwiftData)
    const dataManager = generateDataManager(model);
    if (dataManager) allFiles.push(dataManager);

    // 2. Views
    const viewFiles = generateSwiftUIViews(model);
    allFiles.push(...viewFiles);

    // 3. Navigation
    const navFiles = generateNavigation(model);
    allFiles.push(...navFiles);

    // 4. Networking
    const networkFiles = generateNetworkingLayer(model);
    allFiles.push(...networkFiles);

    // 4b. Error UI Components (ErrorBannerView, RetryButton, EmptyStateView, OfflineBannerView)
    const componentFiles = generateErrorUIComponents();
    allFiles.push(...componentFiles);

    // 5. State
    const stateFiles = generateStateLayer(model);
    allFiles.push(...stateFiles);

    // 6. App entry
    allFiles.push(generateAppEntry(model));

    // 7. Info.plist
    allFiles.push(generateInfoPlist(model));

    // 8. Assets
    const assetFiles = generateAssets(model);
    allFiles.push(...assetFiles);

    // 9. Tests
    const testFiles = generateTestStubs(model);
    allFiles.push(...testFiles);

    // 10. Package.swift (at output root, one level above app folder)
    allFiles.push(generatePackageSwift(model));

    // 11. Xcode workspace settings (opens cleanly in Xcode)
    allFiles.push(generateWorkspaceSettings());

    // 12b. .claude/commands/ (slash commands for AI assistant workflow)
    const commandFiles = generateClaudeCommands(model);
    allFiles.push(...commandFiles);

    // 12c. .claude/settings.json (MCP server auto-registration)
    allFiles.push(generateClaudeSettings());

    // --- Calculate stats ---

    const stats: GeneratedProject['stats'] = {
        totalFiles: allFiles.length,
        highConfidence: allFiles.filter((f) => f.confidence === 'high').length,
        mediumConfidence: allFiles.filter((f) => f.confidence === 'medium').length,
        lowConfidence: allFiles.filter((f) => f.confidence === 'low').length,
        warnings: [],
    };

    // Collect all warnings
    for (const file of allFiles) {
        for (const w of file.warnings) {
            stats.warnings.push(`[${file.path}] ${w}`);
        }
    }

    // 12. README (needs stats)
    allFiles.push(generateReadme(model, stats));

    // 13. CLAUDE.md (AI-assistant context — needs stats for warnings)
    allFiles.push(generateClaudeMd(model, stats));

    // Update total
    stats.totalFiles = allFiles.length;

    // --- Write files to disk ---

    const resolvedAppDir = resolve(appDir);

    for (const file of allFiles) {
        // Security: reject paths containing traversal sequences or absolute paths
        if (file.path.includes('..') && normalize(file.path).startsWith('..')) {
            // Allow controlled parent references (e.g., ../Package.swift, ../README.md)
            // but verify the resolved path stays within the output directory
            const fullPath = resolve(appDir, file.path);
            const resolvedOutputDir = resolve(outputPath) + '/';
            if (!fullPath.startsWith(resolvedOutputDir) && fullPath !== resolve(outputPath)) {
                allWarnings.push(`[${file.path}] Skipped: path resolves outside output directory`);
                continue;
            }
            const dir = dirname(fullPath);
            await mkdir(dir, { recursive: true });
            await writeFile(fullPath, file.content, 'utf-8');
        } else {
            const fullPath = join(appDir, file.path);
            const normalizedFull = resolve(fullPath);
            // Verify resolved path is within the app directory or output directory
            if (!normalizedFull.startsWith(resolvedAppDir + '/') && normalizedFull !== resolvedAppDir && !normalizedFull.startsWith(resolve(outputPath) + '/')) {
                allWarnings.push(`[${file.path}] Skipped: path resolves outside output directory`);
                continue;
            }
            const dir = dirname(fullPath);
            await mkdir(dir, { recursive: true });
            await writeFile(fullPath, file.content, 'utf-8');
        }
    }

    // --- Validate Swift syntax ---

    const syntaxWarnings = await validateSwiftSyntax(appDir, allFiles);
    if (syntaxWarnings.length > 0) {
        stats.warnings.push(...syntaxWarnings);
    }

    return {
        appName,
        files: allFiles,
        outputPath: appDir,
        stats,
    };
}

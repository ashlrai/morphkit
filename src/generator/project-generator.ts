/**
 * Format a TypeDefinition as a human-readable shape string for CLAUDE.md.
 * e.g., `{ email: String, password: String }` or `[Product]`
 */
function formatTypeShape(td: TypeDefinition | null | undefined): string {
    if (!td) return 'unknown';
    switch (td.kind) {
        case 'string': return td.typeName ? pascalCase(td.typeName) : 'String';
        case 'number': return 'Number';
        case 'boolean': return 'Bool';
        case 'date': return 'Date';
        case 'array':
            return td.elementType ? `[${formatTypeShape(td.elementType)}]` : '[Any]';
        case 'object': {
            if (td.typeName) return pascalCase(td.typeName);
            if (td.fields && td.fields.length > 0) {
                const fieldStrs = td.fields.slice(0, 8).map(f =>
                    `${f.name}: ${formatTypeShape(f.type)}${f.optional ? '?' : ''}`
                );
                const suffix = td.fields.length > 8 ? ', ...' : '';
                return `{ ${fieldStrs.join(', ')}${suffix} }`;
            }
            return 'Object';
        }
        case 'enum':
            if (td.values && td.values.length > 0) {
                return td.values.slice(0, 6).map(v => `"${v}"`).join(' | ');
            }
            return td.typeName ? pascalCase(td.typeName) : 'String';
        case 'union':
            if (td.values && td.values.length > 0) {
                return td.values.slice(0, 6).map(v => `"${v}"`).join(' | ');
            }
            return td.typeName ? pascalCase(td.typeName) : 'Any';
        default:
            return 'Any';
    }
}

/** Clean an API URL for display — strip template literals, normalize params */
function cleanEndpointUrl(url: string): string {
    return url
        .replace(/`/g, '')
        .replace(/\$\{[^}]+\}/g, (match) => {
            const inner = match.slice(2, -1);
            // Keep path param references readable
            if (inner.includes('.') || inner.includes('[')) return ':param';
            return `:${inner}`;
        })
        .replace(/\/+/g, '/');
}


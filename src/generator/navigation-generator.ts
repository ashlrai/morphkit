// Generates SwiftUI navigation structure from Semantic App Model

import type {
    SemanticAppModel,
    Screen,
    NavigationFlow,
    TabItem,
    ConfidenceLevel,
} from '../semantic/model';

import type { GeneratedFile } from './swiftui-generator';
import { pascalCase, camelCase, indent } from './swiftui-generator';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Check if a screen needs an id parameter (detail layout with data requirements) */
function screenNeedsIdParam(screen: Screen): boolean {
    return screen.layout === 'detail' && screen.dataRequirements.length > 0;
}

/** Derive an entity-like name from a screen's first data requirement source */
function deriveEntity(screen: Screen): string | undefined {
    return screen.dataRequirements[0]?.source;
}

// ---------------------------------------------------------------------------
// Route enum generation
// ---------------------------------------------------------------------------

/** Check if a route has dynamic params (e.g. :id) */
function routeHasDynamicParams(model: SemanticAppModel, screen: Screen): boolean {
    const routes = model.navigation?.routes ?? [];
    const matchingRoute = routes.find((r) => r.screen === screen.name && r.params.length > 0);
    return !!matchingRoute;
}

/** Get a unique case name for a screen, appending "Detail" for dynamic-param routes */
function getRouteCaseName(model: SemanticAppModel, screen: Screen): string {
    const baseName = camelCase(screen.name);
    if (routeHasDynamicParams(model, screen) || screenNeedsIdParam(screen)) {
        // Avoid double "Detail" suffix — the builder's routePathToScreenName may have already appended it
        if (baseName.endsWith('Detail')) {
            return baseName;
        }
        return `${baseName}Detail`;
    }
    return baseName;
}

function generateRouteEnum(model: SemanticAppModel): string {
    const screens = model.screens ?? [];
    const lines: string[] = [];
    const seenCases = new Set<string>();

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import Foundation');
    lines.push('');
    lines.push('enum AppRoute: Hashable {');

    for (const screen of screens) {
        const caseName = getRouteCaseName(model, screen);
        if (seenCases.has(caseName)) continue;
        seenCases.add(caseName);

        const isDynamic = routeHasDynamicParams(model, screen) || screenNeedsIdParam(screen);
        if (isDynamic) {
            lines.push(`    case ${caseName}(id: String)`);
        } else {
            lines.push(`    case ${caseName}`);
        }
    }

    lines.push('}');

    return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Router / NavigationCoordinator
// ---------------------------------------------------------------------------

function generateRouter(model: SemanticAppModel): string {
    const screens = model.screens ?? [];
    const nav = model.navigation;
    const lines: string[] = [];

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import SwiftUI');
    lines.push('import Observation');
    lines.push('');
    lines.push('@Observable');
    lines.push('final class Router {');
    lines.push('    var path = NavigationPath()');
    lines.push('    var selectedTab: AppTab = .home');
    lines.push('    var presentedSheet: AppRoute?');
    lines.push('');
    lines.push('    func navigate(to route: AppRoute) {');
    lines.push('        path.append(route)');
    lines.push('    }');
    lines.push('');
    lines.push('    func pop() {');
    lines.push('        guard !path.isEmpty else { return }');
    lines.push('        path.removeLast()');
    lines.push('    }');
    lines.push('');
    lines.push('    func popToRoot() {');
    lines.push('        path = NavigationPath()');
    lines.push('    }');
    lines.push('');
    lines.push('    func present(_ route: AppRoute) {');
    lines.push('        presentedSheet = route');
    lines.push('    }');
    lines.push('');
    lines.push('    func dismissSheet() {');
    lines.push('        presentedSheet = nil');
    lines.push('    }');
    lines.push('');
    lines.push('    // MARK: - Deep Linking');
    lines.push('');
    lines.push('    func handleDeepLink(_ url: URL) {');
    lines.push('        guard let components = URLComponents(url: url, resolvingAgainstBaseURL: false) else { return }');
    lines.push('        let pathComponents = components.path.split(separator: "/").map(String.init)');
    lines.push('');
    lines.push('        switch pathComponents.first {');

    const seenDeepLinkCases = new Set<string>();
    for (const screen of screens) {
        const caseName = getRouteCaseName(model, screen);
        if (seenDeepLinkCases.has(caseName)) continue;
        seenDeepLinkCases.add(caseName);

        const isDynamic = routeHasDynamicParams(model, screen) || screenNeedsIdParam(screen);
        if (isDynamic) {
            lines.push(`        case "${screen.name}":`);
            lines.push(`            if let id = pathComponents.dropFirst().first {`);
            lines.push(`                navigate(to: .${caseName}(id: id))`);
            lines.push('            }');
        } else {
            lines.push(`        case "${screen.name}":`);
            lines.push(`            navigate(to: .${caseName})`);
        }
    }

    lines.push('        default:');
    lines.push('            break');
    lines.push('        }');
    lines.push('    }');
    lines.push('}');

    return lines.join('\n');
}


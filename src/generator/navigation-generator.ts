function generateContentView(model: SemanticAppModel): string {
    const navType = model.navigation?.type ?? inferNavigationType(model);
    const appName = pascalCase(model.appName ?? 'App');

    switch (navType) {
        case 'tab':
            return generateTabContentView(model, appName);
        case 'stack':
            return generateStackContentView(model, appName);
        case 'mixed':
            return generateMixedContentView(model, appName);
        default:
            return generateStackContentView(model, appName);
    }
}

function inferNavigationType(model: SemanticAppModel): string {
    // Use inferred tabs count (based on top-level routes) rather than just isEntryPoint
    const tabs = inferTabs(model);
    if (tabs.length >= 2) return 'tab';
    return 'stack';
}

function generateTabContentView(model: SemanticAppModel, appName: string): string {
    // Always use inferred tabs — model.navigation.tabs is often incomplete
    const tabs = inferTabs(model);
    const lines: string[] = [];

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import SwiftUI');
    lines.push('');
    lines.push('struct ContentView: View {');
    lines.push('    @State private var router = Router()');
    lines.push('');
    lines.push('    var body: some View {');
    lines.push('        TabView(selection: $router.selectedTab) {');

    for (const tab of tabs) {
        const viewName = `${pascalCase(tab.screen ?? tab.label)}View`;
        const tabCase = camelCase(tab.label);
        lines.push(`            ${viewName}()`);
        lines.push(`                .tabItem {`);
        lines.push(`                    Label(AppTab.${tabCase}.title, systemImage: AppTab.${tabCase}.systemImage)`);
        lines.push(`                }`);
        lines.push(`                .tag(AppTab.${tabCase})`);
    }

    lines.push('        }');
    lines.push('        .environment(router)');
    lines.push('        .onOpenURL { url in');
    lines.push('            router.handleDeepLink(url)');
    lines.push('        }');
    lines.push('    }');
    lines.push('}');
    lines.push('');
    lines.push('#Preview {');
    lines.push('    ContentView()');
    lines.push('}');

    return lines.join('\n');
}

function generateStackContentView(model: SemanticAppModel, appName: string): string {
    const screens = model.screens ?? [];
    const rootScreen = screens.find((s) => s.isEntryPoint) ?? screens[0];
    const lines: string[] = [];

    lines.push('// Generated by Morphkit');
    lines.push('');
    lines.push('import SwiftUI');
    lines.push('');
    lines.push('struct ContentView: View {');
    lines.push('    @State private var router = Router()');
    lines.push('');
    lines.push('    var body: some View {');
    lines.push('        NavigationStack(path: $router.path) {');

    if (rootScreen) {
        lines.push(`            ${pascalCase(rootScreen.name)}View()`);
    } else {
        lines.push('            Text("Welcome")');
    }

    lines.push('                .navigationDestination(for: AppRoute.self) { route in');
    lines.push('                    routeView(for: route)');
    lines.push('                }');
    lines.push('        }');
    lines.push('        .environment(router)');
    lines.push('        .onOpenURL { url in');
    lines.push('            router.handleDeepLink(url)');
    lines.push('        }');
    lines.push('    }');
    lines.push('');
    lines.push('    @ViewBuilder');
    lines.push('    private func routeView(for route: AppRoute) -> some View {');
    lines.push('        switch route {');

    const seenStackCases = new Set<string>();
    for (const screen of screens) {
        const caseName = getRouteCaseName(model, screen);
        if (seenStackCases.has(caseName)) continue;
        seenStackCases.add(caseName);

        const viewName = `${pascalCase(screen.name)}View`;
        const isDynamic = routeHasDynamicParams(model, screen) || screenNeedsIdParam(screen);
        if (isDynamic) {
            lines.push(`        case .${caseName}(let id):`);
            lines.push(`            ${viewName}(id: id)`);
        } else {
            lines.push(`        case .${caseName}:`);
            lines.push(`            ${viewName}()`);
        }
    }

    lines.push('        }');
    lines.push('    }');
    lines.push('}');
    lines.push('');
    lines.push('#Preview {');
    lines.push('    ContentView()');
    lines.push('}');

    return lines.join('\n');
}

